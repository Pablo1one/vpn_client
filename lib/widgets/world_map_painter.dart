class WorldMapPainter extends CustomPainter {
  final Color landColor;
  final Color gridColor;

  const WorldMapPainter({required this.landColor, required this.gridColor});

  // x = (lon + 180) / 360,   y = (90 - lat) / 180
  // Все полигоны - по часовой стрелке (clockwise) для корректной заливки

  @override
  void paint(Canvas canvas, Size size) {
    // Сетка
    final gp = Paint()
      ..color = gridColor
      ..style = PaintingStyle.stroke
      ..strokeWidth = 0.4;
    for (var lat = -60.0; lat <= 90.0; lat += 30) {
      final y = (90 - lat) / 180 * size.height;
      canvas.drawLine(Offset(0, y), Offset(size.width, y), gp);
    }
    for (var lon = -180.0; lon <= 180.0; lon += 30) {
      final x = (lon + 180) / 360 * size.width;
      canvas.drawLine(Offset(x, 0), Offset(x, size.height), gp);
    }

    final fill = Paint()
      ..color = landColor
      ..style = PaintingStyle.fill;
    final stroke = Paint()
      ..color = Color.lerp(landColor, Colors.black, 0.18)!.withOpacity(landColor.opacity)
      ..style = PaintingStyle.stroke
      ..strokeWidth = 0.6;

    for (final pts in _polygons) {
      final path = Path();
      path.moveTo(pts[0][0] * size.width, pts[0][1] * size.height);
      for (var i = 1; i < pts.length; i++) {
        path.lineTo(pts[i][0] * size.width, pts[i][1] * size.height);
      }
      path.close();
      canvas.drawPath(path, fill);
      canvas.drawPath(path, stroke);
    }
  }

  @override
  bool shouldRepaint(WorldMapPainter o) =>
      o.landColor != landColor || o.gridColor != gridColor;

