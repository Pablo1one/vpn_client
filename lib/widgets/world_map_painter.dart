  @override
  void paint(Canvas canvas, Size size) {
    // Grid
    final gridPaint = Paint()
      ..color = gridColor
      ..style = PaintingStyle.stroke
      ..strokeWidth = 0.4;

    for (var lat = -60.0; lat <= 90.0; lat += 30) {
      final y = (90 - lat) / 180 * size.height;
      canvas.drawLine(Offset(0, y), Offset(size.width, y), gridPaint);
    }
    for (var lon = -150.0; lon <= 180.0; lon += 30) {
      final x = (lon + 180) / 360 * size.width;
      canvas.drawLine(Offset(x, 0), Offset(x, size.height), gridPaint);
    }

    // Continents
    final landPaint = Paint()
      ..color = landColor
      ..style = PaintingStyle.fill;

    for (final polygon in _land) {
      if (polygon.isEmpty) continue;
      final path = Path()
        ..moveTo(polygon[0][0] * size.width, polygon[0][1] * size.height);
      for (var i = 1; i < polygon.length; i++) {
        path.lineTo(polygon[i][0] * size.width, polygon[i][1] * size.height);
      }
      path.close();
      canvas.drawPath(path, landPaint);
    }
  }

  @override
  bool shouldRepaint(WorldMapPainter old) =>
      old.landColor != landColor || old.gridColor != gridColor;
}
